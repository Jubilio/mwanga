const crypto = require('crypto');
const { z } = require('zod');
const { db } = require('../config/db');
const logger = require('../utils/logger');

function generateRandomCode(length = 6) {
  const chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789';
  let result = '';
  for (let i = 0; i < length; i++) {
    result += chars.charAt(Math.floor(Math.random() * chars.length));
  }
  return result;
}

const generateInvite = async (req, res, next) => {
  try {
    const householdId = req.user.householdId;
    
    // Check if there is already an active invite to avoid spamming
    const existingResult = await db.execute({
      sql: 'SELECT invite_code, expires_at FROM household_invites WHERE household_id = $1 AND status = $2 AND expires_at > NOW()',
      args: [householdId, 'active']
    });
    
    if (existingResult.rows.length > 0) {
      return res.json({ 
        inviteCode: existingResult.rows[0].invite_code,
        expiresAt: existingResult.rows[0].expires_at,
        message: 'Convite ativo recuperado.'
      });
    }

    const inviteCode = generateRandomCode(8); // A bit longer to ensure uniqueness
    
    await db.execute({
      sql: `INSERT INTO household_invites (household_id, invite_code, created_by) 
            VALUES ($1, $2, $3)`,
      args: [householdId, inviteCode, req.user.id]
    });
    
    logger.info(`New household invite generated by user ${req.user.id}: ${inviteCode}`);
    
    res.status(201).json({
      inviteCode,
      message: 'Novo convite gerado com sucesso.'
    });
  } catch (error) {
    if (error.message && error.message.includes('UNIQUE constraint')) {
      // Very unlucky collision, try again
      return generateInvite(req, res, next);
    }
    next(error);
  }
};

const getFamilyMembers = async (req, res, next) => {
  try {
    const householdId = req.user.householdId;
    
    const result = await db.execute({
      sql: 'SELECT id, name, email, role, created_at FROM users WHERE household_id = $1',
      args: [householdId]
    });
    
    res.json({ members: result.rows });
  } catch (error) {
    next(error);
  }
};

module.exports = {
  generateInvite,
  getFamilyMembers
};
